// 문제1
// for문을 이용하여 1부터 100사이의 짝수를 모두 더 하시오
// 1. for문 사용 => 초기화(let i =2), 조건(i <= 100), 증감(i +=2)
// 2. sum += i

let sum = 0;
for (let i = 2; i <= 100; i += 2) {
  sum = sum + i;
}

// console.log(sum);

//문제2
// 위에서 풀이한 문제를 i를 1씩 더하면서 if문을 이용하여 짝수만 더한 결과를 내시오.
//1. 짝수만 고른다(if)
//2. 고른 짝수를 sum에 더한다(sum += i)

let sum2 = 0;
for (let i = 1; i <= 100; i += 1) {
  if (i % 2 === 0) {
    sum2 += i;
  }
}

