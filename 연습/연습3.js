// 문제 3
// 주어진 학점으로 평균을 구하시오
// A: 4점, B:3점, C:2점, 그 외: 0점
// 문제 나누기
//1. 배열에서 학점 꺼내기
//2. 각 학점에 배치된 점수를 총 점수에 더한다.
//3. 총 점수를 과목 수(5)로 나눈다
const score = ["A", "B", "A", "C", "A"];
// console.log(score.length);

let total = 0;
for (let i = 0; i < score.length; i++) {
  let s = score[i];
  if (s === "A") {
    total += 4;
  } else if (s === "B") {
    total += 3;
  } else if (s === "C") {
    total += 2;
  }
}

// console.log("평균: ", total / score.length);

